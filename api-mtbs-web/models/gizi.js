const classifierGizi = (ansGizi) => {
  if (
    (ansGizi.gizi_tampakSangatKurus === true ||
      ansGizi.gizi_pembengkakanKeduaPunggungKakiAtauTangan === true ||
      ansGizi.gizi_BBmenurutPBAtauTB === "BB/PB (TB) < -3 SD (Sangat Kurus)" ||
      ansGizi.gizi_lingkarLenganAtas === "LiLA < 11,5 cm") &&
    (ansGizi.gizi_tandaBahayaUmum === true ||
      ansGizi.gizi_batuk === true ||
      ansGizi.gizi_diare === true ||
      ansGizi.gizi_demam === true ||
      ansGizi.gizi_telinga === true ||
      ansGizi.gizi_masalahPemberianASI === true)
  ) {
    const ans = {
      hasilKlasifikasi: "Sangat kurus dengan komplikasi",
      statusKlasifikasi: "danger",
    };
    return ans;
  } else if (
    ansGizi.gizi_tampakSangatKurus === true ||
    ansGizi.gizi_BBmenurutPBAtauTB === "BB/PB (TB) < -3 SD (Sangat Kurus)" ||
    ansGizi.gizi_lingkarLenganAtas === "LiLA < 11,5 cm"
  ) {
    const ans = {
      hasilKlasifikasi: "Sangat kurus tanpa komplikasi",
      statusKlasifikasi: "warning",
    };
    return ans;
  } else if (
    ansGizi.gizi_BBmenurutPBAtauTB === "-3 SD ≥ BB/PB (TB) ≤ -2 SD (Kurus)" ||
    ansGizi.gizi_lingkarLenganAtas === "LiLA < 11,5 cm - < 12,5 cm"
  ) {
    const ans = {
      hasilKlasifikasi: "Kurus",
      statusKlasifikasi: "warning",
    };
    return ans;
  } else {
    const ans = {
      hasilKlasifikasi: "Gizi normal",
      statusKlasifikasi: "success",
    };
    return ans;
  }
};

